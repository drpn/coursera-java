package generics;

/*
 * What are generics? 
 * "abstraction over types" - during compile time.
 * 
 * Why generics?
 * 
 * 1. Generics allow us to write generic code without losing type safety.
 * 2. They can be used to write generic type declarations while creating classes, interfaces and methods;
 * that can work with different type of data types.
 * 3. compile-time type checking, to avoid runtime typecasting exceptions
 * 
 * IMPORTANT: There are no generics at runtime - actual types replaces generic types during compilation
 */

